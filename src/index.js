const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let decText = '';
    for (let i = 0; i < expr.length; i += 10) {
        let partExpr = expr.slice(i, i + 10);
        if(partExpr === '**********') {
            decText += ' ';
        } else {
            partExpr = partExpr.replaceAll('00', '').replaceAll('10', '.').replaceAll('11', '-');
            decText += MORSE_TABLE[partExpr];
        }
    }
    return decText;
}

module.exports = {
    decode
}